func convertResponse(u models.User) usersdto.UserResponse {
	return usersdto.UserResponse{
		ID:       u.ID,
		FullName: u.FullName,
		Email:    u.Email,
		Password: u.Password,
		Phone:    u.Phone,
		Location: u.Location,
		Image:    u.Image,
		Role:     u.Role,
	}
}
